import re
phoneRegex = re.compile(r'''(
            (\d{3}|\(d{3}\))?            #区号两种形式三个数或带括号的三个数，可选
            (\s|-|\.)?                   #分隔符三种，空格，-，.，可选
            \d{3}                        #前三位数，必选
            (\s|-|\.)                    #分隔符三种，空格，-，.，必选
            \d{4}                        #后四位数，必选
            (\s*(ext|x|ext.)\s*\d{2,5})? #分机号，任意空格，ext或x或ext.，任意空格，2-5位数，可选
            )''', re.VERBOSE)
